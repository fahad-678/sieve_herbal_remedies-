  static Herb? getHerbById(String id) {
    try {
      return herbs.firstWhere((herb) => herb.id == id);
    } catch (e) {
      return null;
    }
  }

  static List<Herb> getHerbsByCategory(String category) {
    return herbs.where((herb) => herb.category == category).toList();
  }

  static List<String> getAllCategories() {
    return herbs.map((herb) => herb.category).toSet().toList()..sort();
  }

  static List<Herb> searchHerbs(String query) {
    final lowerQuery = query.toLowerCase();
    return herbs.where((herb) {
      return herb.name.toLowerCase().contains(lowerQuery) ||
          herb.scientificName.toLowerCase().contains(lowerQuery) ||
          herb.category.toLowerCase().contains(lowerQuery) ||
          herb.briefDescription.toLowerCase().contains(lowerQuery) ||
          herb.primaryBenefits.any((b) => b.toLowerCase().contains(lowerQuery));
    }).toList();
  }

  static List<Herb> getFeaturedHerbs() {
    return herbs.where((herb) => herb.isFeatured).toList();
  }
